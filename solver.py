def solve(bo):

    find = find_empty(bo)
    if not find:
        return True
    else:
        row, col = find

    for i in range(1, 10):
        if valid(bo, i, (row, col)):
            bo[row][col] = i

            if solve(bo):
                return True

            # backtrack
            bo[row][col] = 0

    return False





def valid(bo, num, pos):
    """Note that we record pos in the form of (row, col) in the find_empty(bo) function"""
    # Check row
    for i in range(len(bo[0])):
        # Check each element in the current row to see if it's equal to the num we just inserted, but
        # ignore the position that we inserted the num
        if bo[pos[0]][i] == num and pos[1] != i:
            return False

    # Check column
    for i in range(len(bo)):
        if bo[i][pos[1]] == num and pos[0] != i:
            return False

    # Check box
    # If box_x = 1, box_y = 0, (box_x, box_y) would be the middle box at the very top
    box_x = pos[1] // 3
    box_y = pos[0] // 3

    # box_x and box_y ranges from 0 to 2. So we can access a certain box by multiplying them by 3
    for i in range(box_y * 3, box_y * 3 + 3):
        for j in range(box_x * 3, box_x * 3 + 3):
            if bo[i][j] == num and (i, j) != pos:
                return False
    return True



